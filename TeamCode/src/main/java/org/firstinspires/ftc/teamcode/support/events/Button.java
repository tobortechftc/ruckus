package org.firstinspires.ftc.teamcode.support.events;

/**
 * Represents on / off buttons on gamepad controller.
 * Note that although DPAD buttons are analog, they're treated as on / off by Gamepad class
 */
public enum Button {
    DPAD_UP,
    DPAD_DOWN,
    DPAD_LEFT,
    DPAD_RIGHT,
    A,
    B,
    X,
    Y,
    START,
    BACK,
    LEFT_BUMPER,
    RIGHT_BUMPER,
    LEFT_STICK,  // push button on left stick
    RIGHT_STICK  // push button on right stick
}